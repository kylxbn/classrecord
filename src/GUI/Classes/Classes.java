    public void refresh()
    {
        try {
            try
            {
                Integer.parseInt(txtSY.getText());
            }
            catch (Exception ex)
            {
                txtSY.setText("0");
            }
            try
            {
                Integer.parseInt(txtSem.getText());
            }
            catch (Exception ex)
            {
                txtSem.setText("0");
            }
            tblClasses.setModel(DB.getClassesTable(txtName.getText(), Integer.parseInt(txtSY.getText()), Integer.parseInt(txtSem.getText()), txtCourse.getText()));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "An internal error has occurred:\n" + ex.getMessage(), "Internal error", JOptionPane.WARNING_MESSAGE);
            Logger.getLogger(Classes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
