    private void cmdEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdEditActionPerformed
        int cid;
        cid = tblClasses.getSelectedRow();
        if (cid < 0)
        {
            JOptionPane.showMessageDialog(this, "Select a class to edit first.", "Error", JOptionPane.ERROR_MESSAGE); 
            return;
        }
        new EditClass(id, caps, (int)tblClasses.getValueAt(cid, 0), this).setVisible(true);
    }//GEN-LAST:event_cmdEditActionPerformed

    private void cmdDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdDeleteActionPerformed
        int cid;
        cid = tblClasses.getSelectedRow();
        if (cid < 0)
        {
            JOptionPane.showMessageDialog(this, "Select a class to delete first.", "Error", JOptionPane.ERROR_MESSAGE); 
            return;
        }
        try {
            DB.deleteClass((int)tblClasses.getValueAt(cid, 0));
            refresh();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "An internal error has occurred:\n" + ex.getMessage(), "Internal error", JOptionPane.WARNING_MESSAGE);
            Logger.getLogger(Classes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cmdDeleteActionPerformed

