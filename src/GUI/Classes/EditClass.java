    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            try
            {
                Integer.parseInt(txtSY.getText());
            }
            catch (Exception ex)
            {
                txtSY.setText("0");
            }
            try
            {
                Integer.parseInt(txtSem.getText());
            }
            catch (Exception ex)
            {
                txtSem.setText("0");
            }
            try
            {
                Integer.parseInt(txtYear.getText());
            }
            catch (Exception ex)
            {
                txtYear.setText("0");
            }
            DB.editClass(cid, txtName.getText(), Integer.parseInt(txtSY.getText()), Integer.parseInt(txtSem.getText()), Integer.parseInt(txtYear.getText()), txtCourse.getText(), txtRoom.getText(), txtSched.getText(), cboLevel.getItemAt(cboLevel.getSelectedIndex()));
            caller.refresh();
            this.dispose();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "An internal error has occurred:\n" + ex.getMessage(), "Internal error", JOptionPane.WARNING_MESSAGE);
            Logger.getLogger(Classes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

