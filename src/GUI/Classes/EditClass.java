    public EditClass(int i, int c, int ci, Classes ca) {
        initComponents();
        
        id = i;
        caps = c;
        cid = ci;
        caller = ca;
        
        Data.Class cla = null;
        try {
            cla = DB.getClass(cid);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "An internal error has occurred:\n" + ex.getMessage(), "Internal error", JOptionPane.WARNING_MESSAGE);
            Logger.getLogger(EditClass.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        txtName.setText(cla.getName());
        txtSY.setText(Integer.toString(cla.getSchoolYear()));
        txtSem.setText(Integer.toString(cla.getSem()));
        txtYear.setText(Integer.toString(cla.getYear()));
        txtCourse.setText(cla.getCourse());
        txtRoom.setText(cla.getRoom());
        txtSched.setText(cla.getSched());
        if ("College".equals(cla.getLevel()))
            cboLevel.setSelectedIndex(0);
        else
            cboLevel.setSelectedIndex(1);
        
        this.setLocationRelativeTo(null);
    }

